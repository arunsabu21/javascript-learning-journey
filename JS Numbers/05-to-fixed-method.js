// The toFixed() Method
// toFixed() returns a string, with the number written with a specified number of decimals:A
let x = 9.656;
let fixed0 = x.toFixed(0);
let fixed2 = x.toFixed(2);
let fixed4 = x.toFixed(4);
let fixed6 = x.toFixed(6);
console.log(fixed0 + " " + fixed2 + " " + fixed4 + " " + fixed6)