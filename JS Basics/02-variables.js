// JavaScript Variables
// Using let & Using const

// using let
let x = 5;
let y = 6;
let z = x + y;
console.log(z)

// using const
const a = 5;
const b = 6;
const c = a + b;
console.log(c)

let carName = "BMW";
console.log(carName)

const price1 = 5;
const price2 = 6;
let total = price1 + price2;
console.log(total)

// You can declare many variables in one statement.

let person = "John Doe", nameCar = "Porsche", price = 200;
console.log(nameCar)
console.log(person)
console.log(price)

let r = 5 + 2 + 3;
console.log(r)

let m = "5" + 2 + 3;
console.log(m)
