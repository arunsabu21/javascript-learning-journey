// Sorting an Array
// The sort() method sorts an array alphabetically:

// The sort() Method
const fruits = ["Banana", "Orange", "Apple", "Mango"];
console.log(fruits); // Output: [ 'Banana', 'Orange', 'Apple', 'Mango' ]

fruits.sort();
console.log(fruits); // Output: [ 'Apple', 'Banana', 'Mango', 'Orange' ]